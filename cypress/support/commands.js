Cypress.Commands.add('login', () => {
  cy.request({
    method: 'POST',
    url: 'http://localhost:4000/login',
    body: {
      email: 'andras@surname.com',
      password: 'Iamandras',
    }
  })
  .then((resp) => {
    window.localStorage.setItem('jwt', resp.body.token)
  })
})
